    private void btnXmlutilsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXmlutilsActionPerformed
        
        if(Xmlsource==null) {JOptionPane.showMessageDialog(this, "Nenurodyti pertvarkomi failai!");   return;   }
        if(Xmlresultdir==null) {JOptionPane.showMessageDialog(this, "Nenurodytas pertvarkytų failų aplankas!");   return;   }
        
        if(!Xmlsource.exists()) {JOptionPane.showMessageDialog(this, "Nėra XML failų!");   return;   }
        if(!Xmlresultdir.exists()) {JOptionPane.showMessageDialog(this, "Nėra pertvarkytų failų aplanko!");   return;   }
        
        
        if(Xmlsource.isFile()) {
            
            if (!Perkodavimas.getExtension(Xmlsource).equalsIgnoreCase("xml") ) {
           JOptionPane.showMessageDialog(this, "Turi būti XML failas!");
           return;}

      
            try {
                String s = XmlUtils.formatXml(Perkodavimas.readFile(Xmlsource.getAbsolutePath()), 1);   
    BufferedWriter out = new BufferedWriter(new FileWriter(Xmlresultdir.getAbsolutePath()+ System.getProperty("file.separator") + "F_" + Xmlsource.getName())) ;
            out.write(s);
           out.close();   
   System.out.println(Xmlsource.getName());
            } //if file
            catch (IOException ex) {
                Logger.getLogger(Forma.class.getName()).log(Level.SEVERE, null, ex);
            }
             
            }//if file
            
            if(Xmlsource.isDirectory()) {
                
                
                
                                
                 try { 
                File[] files = Xmlsource.listFiles();
                
                int viso = files.length;
                int dabar = 0;
                
            for (File xml : files)  {
                if (!Perkodavimas.getExtension(xml).equalsIgnoreCase("xml") ) continue;
                dabar++;
                setTitle("Formatuojamas " + dabar + "-as failas " + xml.getName()+" iš "+viso);
                   
            String s = XmlUtils.formatXml(Perkodavimas.readFile(xml.getAbsolutePath()), 1);   
    BufferedWriter out = new BufferedWriter(new FileWriter(Xmlresultdir.getAbsolutePath()+ System.getProperty("file.separator") +"F_"+ xml.getName())) ;
            out.write(s);
           out.close();   
   System.out.println(xml.getName());
            }// for
                } catch (IOException ex){Logger.getLogger(Forma.class.getName()).log(Level.SEVERE, null, ex);}
                      }//if directory
        
  JOptionPane.showMessageDialog(this, "XML failai pertvarkyti!");      
    }//GEN-LAST:event_btnXmlutilsActionPerformed

